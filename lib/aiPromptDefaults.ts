/** 플레이스홀더 {{key}} 를 values[key] 로 치환. 없으면 빈 문자열 */
export function applyPromptTemplate(template: string, values: Record<string, string | number>): string {
  return template.replace(/\{\{(\w+)\}\}/g, (_, key: string) => {
    if (Object.prototype.hasOwnProperty.call(values, key)) {
      return String(values[key]);
    }
    return "";
  });
}
